     * @param $name
     * @return null|Input
     */
    public function getInput($name)
    {
        $value = $this->get($name);
        if (is_null($value)) {
            $value = $this->post($name);
        }
        if (is_null($value)) {
            return null;
        }
        return new Input($value);
    }

